// ============================================================
// KET 阅读理解模块 - 数据文件
// 包含 KET Reading Part 1–5 共 5 大题型
// ============================================================

// ==================== 类型定义 ====================

/** Part 1: 看图配对 — 5 张图 + 8 个共享句子（A-H），为每张图选匹配的句子 */
export interface Part1Question {
  id: string;
  /** 图片描述（用 emoji 或文字表示图片内容） */
  imageDesc: string;
  /** 图片 emoji */
  emoji: string;
  answer: string;          // 正确的句子（必须是 article.sentences 之一）
}

export interface Part1Article {
  id: string;
  title: string;
  titleZh: string;
  difficulty: 'easy' | 'medium' | 'hard';
  topic: string;
  sentences: string[];     // 8 个共享句子（A-H），含 5 个正确句 + 3 个干扰句
  questions: Part1Question[];
}

/** Part 3-1: 完形填空 — 短文 + 6 个空格，三选一 */
export interface ClozeBlank {
  id: string;              // blank id
  position: number;        // 在文章中的顺序位置
  options: string[];       // 3 个选项
  answer: string;          // 正确答案
  explanation: string;     // 解析
}

export interface Part3ClozeArticle {
  id: string;
  title: string;
  titleZh: string;
  passage: string;         // 完整短文（空格用 ____ 表示）
  passageFull: string;     // 完整短文（填好答案，用于展示解析）
  blanks: ClozeBlank[];
  difficulty: 'easy' | 'medium' | 'hard';
  topic: string;
}

/** Part 3-2: 阅读理解选择题 — 文章 + 7 道四选一 */
export interface ReadingChoice {
  id: string;
  question: string;
  questionZh: string;
  options: string[];       // 4 个选项 A/B/C/D
  answer: string;          // 正确答案字母或文字
  explanation: string;     // 解析
}

export interface Part3RCArticle {
  id: string;
  title: string;
  titleZh: string;
  article: string;         // 文章正文
  articleZh: string;       // 中文参考译文
  questions: ReadingChoice[];
  difficulty: 'easy' | 'medium' | 'hard';
  topic: string;
}

/** Part 4/5: 正误判断 — 长文 + T/F/DN 陈述 */
export interface TFAStatement {
  id: string;
  statement: string;
  statementZh: string;
  answer: 'T' | 'F' | 'DN';  // T/F/DN
  explanation: string;
  evidence?: string;         // 原文依据（可选，UI 中若有则展示）
}

export interface Part4TFArticle {
  id: string;
  title: string;
  titleZh: string;
  article: string;         // 完整长文
  articleZh: string;      // 中文参考译文
  statements: TFAStatement[];
  difficulty: 'easy' | 'medium' | 'hard';
  topic: string;
}

/** Part 5: 单词填写 — 短文 + 5 个单词填空 */
export interface Part5Blank {
  id: string;
  position: number;
  answer: string;
  hint: string;           // 提示（英文解释）
  hintZh: string;         // 提示中文
  accept?: string[];      // 可选：其他可接受答案（开放填空常有多种填法）
}

export interface Part5Article {
  id: string;
  title: string;
  titleZh: string;
  passage: string;         // 完整短文（空格用 ____ 表示）
  passageFull: string;     // 完整短文（填好单词）
  blanks: Part5Blank[];
  difficulty: 'easy' | 'medium' | 'hard';
  topic: string;
}

/** Part 2: 信息匹配 — 5个人物 + 8则信息，选出谁需要什么 */
export interface Part2Person {
  id: string;
  name: string;
  description: string;     // 人物的简短描述（用于显示）
}

export interface Part2Article {
  id: string;
  title: string;
  titleZh: string;
  difficulty: 'easy' | 'medium' | 'hard';
  topic: string;
  people: Part2Person[];  // 5个人物（题目）
  texts: string[];        // 8则信息（选项 A–H）
  answers: number[];      // 长度=5，answers[i]=第 i 个人物匹配的 text 下标(0..7)
}

